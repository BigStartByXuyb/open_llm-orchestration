"""
核心数据类型定义 — CanonicalMessage 及其相关类型
Core data type definitions — CanonicalMessage and related types.

Layer 0: No internal imports allowed (except shared/enums.py).

CanonicalMessage 演进规则（Evolution Rules）:
  ✅ 允许: 新增字段（必须有默认值）
           Add new fields (must have default values)
  ❌ 禁止: 删除字段、重命名字段、修改已有字段类型
           Remove fields, rename fields, or change existing field types
  新增 ContentPart 子类型时，已有 Transformer 收到未知 part 类型时应跳过而非报错。
  When adding new ContentPart subtypes, existing Transformers must skip unknown
  part types rather than raising errors.

schema_version occupies slot for future migration tooling.
schema_version 为未来迁移工具占位。
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any

from orchestration.shared.enums import Capability, ContentPartType, ProviderID, Role, TaskStatus


# ---------------------------------------------------------------------------
# Content Parts / 内容块
# ---------------------------------------------------------------------------


@dataclass(frozen=True)
class TextPart:
    """
    纯文本内容块 / Plain text content part.
    Most common content type. 最常见的内容类型。
    """

    text: str
    type: ContentPartType = ContentPartType.TEXT


@dataclass(frozen=True)
class ImagePart:
    """
    图像内容块 / Image content part.
    Supports both URL references and base64-encoded data.
    支持 URL 引用和 base64 编码数据。
    """

    url: str = ""                  # 图像 URL / Image URL
    data: str = ""                 # base64 编码数据 / Base64-encoded data
    media_type: str = "image/jpeg" # MIME 类型 / MIME type
    type: ContentPartType = ContentPartType.IMAGE


@dataclass(frozen=True)
class ToolCallPart:
    """
    工具调用请求块 / Tool call request part.
    Generated by the assistant when it wants to invoke a tool.
    由 assistant 生成，表示希望调用某个工具。
    """

    tool_call_id: str              # 唯一调用 ID，用于匹配结果 / Unique call ID to match result
    tool_name: str                 # 工具名称 / Tool name
    arguments: dict[str, Any]      # 工具参数（JSON 可序列化）/ Tool arguments (JSON-serializable)
    type: ContentPartType = ContentPartType.TOOL_CALL


@dataclass(frozen=True)
class ToolResultPart:
    """
    工具调用结果块 / Tool call result part.
    Returned by the tool executor to the conversation context.
    由工具执行器返回到对话上下文。
    """

    tool_call_id: str              # 对应的调用 ID / Corresponding call ID
    content: str                   # 结果内容（字符串化）/ Result content (stringified)
    is_error: bool = False         # 是否执行失败 / Whether execution failed
    type: ContentPartType = ContentPartType.TOOL_RESULT


# Union type for all content parts / 所有内容块的联合类型
ContentPart = TextPart | ImagePart | ToolCallPart | ToolResultPart


# ---------------------------------------------------------------------------
# Tool definition / 工具定义
# ---------------------------------------------------------------------------


@dataclass(frozen=True)
class CanonicalTool:
    """
    工具定义（传给 LLM 的工具描述）
    Tool definition passed to LLM for function calling.
    """

    name: str
    description: str
    input_schema: dict[str, Any]    # JSON Schema 格式 / JSON Schema format
    tool_id: str = ""               # 内部 ID，可选 / Internal ID, optional


# ---------------------------------------------------------------------------
# Core message type / 核心消息类型
# ---------------------------------------------------------------------------


@dataclass(frozen=True)
class CanonicalMessage:
    """
    系统统一消息格式 — 跨所有 provider 的标准消息表示
    System-unified message format — canonical representation across all providers.

    This is the lingua franca of the orchestration system. Every provider
    transformer converts FROM this format to their own API format, and
    converts responses BACK to CanonicalMessage / ProviderResult.
    这是编排系统的通用语。每个 provider transformer 将此格式转换为各自的
    API 格式，并将响应转换回 CanonicalMessage / ProviderResult。

    Immutable (frozen=True): prevents accidental mutation in pipeline.
    不可变（frozen=True）：防止管道中意外修改。
    """

    role: Role                           # system | user | assistant | tool
    content: list[ContentPart]           # One or more content parts / 一个或多个内容块
    message_id: str = ""                 # 可选消息 ID / Optional message ID
    schema_version: int = 1              # 演进占位，迁移工具使用 / Evolution placeholder for migration tooling
    metadata: dict[str, Any] = field(default_factory=dict)  # 扩展元数据 / Extension metadata

    def char_count(self) -> int:
        """
        计算消息的字符数（provider 无关）
        Calculate character count for this message (provider-agnostic).

        Used for context truncation decisions. Stored in DB as `char_count`
        on the session, never as token count to remain provider-independent.
        用于上下文截断决策。在 DB 中存储为 session 的 `char_count`，
        而非 token 数，以保持 provider 无关性。
        """
        total = 0
        for part in self.content:
            if isinstance(part, TextPart):
                total += len(part.text)
            elif isinstance(part, ToolCallPart):
                total += len(part.tool_name) + len(str(part.arguments))
            elif isinstance(part, ToolResultPart):
                total += len(part.content)
        return total


