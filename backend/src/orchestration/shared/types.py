@dataclass
class StreamChunk:
    """
    流式响应块 / Streaming response chunk from a provider adapter.
    """

    delta: str                     # 增量文本内容 / Incremental text content
    is_final: bool = False         # 是否为最后一块 / Whether this is the final chunk
    metadata: dict[str, Any] = field(default_factory=dict)


@dataclass
class ProviderResult:
    """
    Provider 调用结果 — 标准化的 provider 响应
    Provider call result — standardized provider response.

    char_count is computed from content (not stored separately in this object),
    used by ResultAggregator for overflow detection.
    char_count 从 content 计算（不单独存储），由 ResultAggregator 用于溢出检测。
    """

    subtask_id: str
    provider_id: ProviderID
    content: str                           # 主文本结果 / Primary text result
    transformer_version: str = "v1"
    tokens_used: int = 0                   # 实际 token 用量（来自 provider 响应头）/ Actual tokens from response
    latency_ms: float = 0.0
    raw_response: dict[str, Any] = field(default_factory=dict)  # 原始响应（调试用）/ Raw response for debugging
    tool_calls: list[Any] = field(default_factory=list)  # ToolCallPart list if LLM requested tools ★ Sprint 11
    metadata: dict[str, Any] = field(default_factory=dict)

    def char_count(self) -> int:
        """内容字符数 / Character count of content."""
        return len(self.content)
