@dataclass
class SubTask:
    """
    由主 LLM 分解出的子任务 / Sub-task decomposed by the coordinator LLM.

    Each subtask is assigned to exactly one provider by the CapabilityRouter.
    每个子任务由 CapabilityRouter 分配给恰好一个 provider。

    context_slice: minimal context the sub-agent needs, explicitly allocated
    by the coordinator. Sub-agents have NO access to full session history.
    context_slice：子 Agent 需要的最小上下文，由协调者明确分配。
    子 Agent 没有访问完整会话历史的权限。
    """

    subtask_id: str
    description: str                              # 人类可读描述 / Human-readable description
    capability: Capability                        # 所需能力 / Required capability
    context_slice: list[CanonicalMessage]         # 分配给子 Agent 的上下文 / Context allocated to sub-agent
    provider_id: ProviderID = ProviderID.ANTHROPIC # CapabilityRouter 填充 / Filled by CapabilityRouter
    transformer_version: str = "v1"              # CapabilityRouter 填充 / Filled by CapabilityRouter
    depends_on: list[str] = field(default_factory=list)  # 依赖的子任务 ID / Dependency subtask IDs
    skill_id: str = ""                            # 若为 Skill 类型，填写 skill_id / Skill ID if SKILL type
    tools: list[Any] = field(default_factory=list)  # CanonicalTool list for function calling ★ Sprint 11
    status: TaskStatus = TaskStatus.PENDING
    metadata: dict[str, Any] = field(default_factory=dict)


@dataclass
class TaskPlan:
    """
    主 LLM 生成的完整任务计划 / Complete task plan generated by coordinator LLM.
    Contains ordered subtasks that ParallelExecutor will schedule.
    包含 ParallelExecutor 将调度的有序子任务。
    """

    plan_id: str
    subtasks: list[SubTask]
    summary: str = ""                             # 计划摘要 / Plan summary
    metadata: dict[str, Any] = field(default_factory=dict)


